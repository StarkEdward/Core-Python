# Relational Operator / Comparision Operator

# less than
print(5<2)
a = 5
b = 2
value = a < b
print(value)

# greater than
print(5>2)
a = 5
b = 2
val = a > b
print(val)

# less than or equal to
print(5<=2)
a = 5
b = 2
val = a <= b
print(val)

# greater than or equal to
print(5>=2)
a = 5
b = 2
val = a >= b
print(val)

# Equal to
print(5 == 2)
a = 5
b = 2
val = a == b
print(val)

# not equal to
print(5!=2)
a = 5
b = 2
val = a != b
print(val)