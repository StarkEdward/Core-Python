stu = {101:"Edward", 102:"Sonu", 103:"Jarvis"}
print("Original Dict:")
print(stu)
print(id(stu))
print()

new_stu = stu.copy()
print("Copied Dict:")
print(new_stu)
print(id(new_stu))
